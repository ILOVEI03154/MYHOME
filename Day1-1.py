# 变量初始化
a = 1  # 定义变量a并赋值为1
b = 1  # 定义变量b并赋值为1
c = 1  # 定义变量c并赋值为1

# 单独打印每个变量（默认换行）
print(a)  # 输出变量a的值
print(b)  # 输出变量b的值
print(c)  # 输出变量c的值

# 默认格式打印（空格分隔）
print(a,b,c)  # 同时打印三个变量，默认用空格分隔

# 指定分隔符打印
print(a,b,c,sep='\n')  # 用换行符分隔，每个变量单独一行

# 格式化字符串打印
print(f'{a}\n{b}\n{c}')  # 使用f-string插入换行符实现分行输出